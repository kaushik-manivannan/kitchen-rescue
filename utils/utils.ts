import { Bounce, toast } from 'react-toastify';

// Prints Toaster Success Message
export function printSuccessMessage (message: string) {
  toast.success(message, {
      position: "bottom-center",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "light",
      transition: Bounce,
    });
}

